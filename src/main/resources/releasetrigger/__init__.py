import logging

# This log file configuration will overwrite previous messages with each run. This prevents the log file from growing. If you want to preserver
#    previous messages, change the filemode to append mode by changing the value  from 'w' to 'a' 
logging.basicConfig(filename='log/releaseTriggerAPI.log',
                            filemode='w',
                            format='%(asctime)s,%(msecs)d %(name)s %(levelname)s %(message)s',
                            datefmt='%H:%M:%S',
                            level=logging.DEBUG)



